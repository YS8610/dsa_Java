  private static int dfs(int[][] grid, int r, int c){
    if (r<0 || c<0 || r>=grid.length ||  c>=grid[0].length) return 0;
    if (grid[r][c]==1){
      grid[r][c]=0;
      return 1 + dfs(grid,r-1,c) + dfs(grid,r+1,c) + dfs(grid,r,c-1) + dfs(grid,r,c+1);
    }
    return 0;
  }
  
  private static int maxAreaOfIsland(int[][] grid) {
    int rlen = grid.length;
    int clen = grid[0].length;
    int maxArea = 0;
    for (int r=0; r<rlen;r++){
      for(int c=0; c<clen;c++){
        if (grid[r][c]==1){
          int area = dfs(grid, r,c);
          maxArea = maxArea>=area? maxArea:area;
        }
      }
    }
    return maxArea;
  }

  private static int maxAreaOfIsland1(int[][] grid){
    Deque<List<Integer>> stack = new ArrayDeque<>();
    int rlen = grid.length;
    int clen = grid[0].length;
    int maxArea = 0;
    for (int r=0; r<rlen;r++){
      for(int c=0; c<clen;c++){
        if (grid[r][c]==1){
          stack.add(List.of(r,c));
          int area = 0;
          Set<List<Integer>> added = new HashSet<>();
          while(!stack.isEmpty()){
            List<Integer> coor = stack.pop();
            int row = coor.get(0);
            int col = coor.get(1);
            if(grid[row][col]==1) area++;
            grid[row][col]=0;
            if (row>0 && grid[row-1][col]==1) {
              List<Integer> loc = List.of(row-1,col);
              if (!added.contains(loc)){
                stack.add(loc);
                added.add(loc);
              }
            }
            if (row<rlen-1 && grid[row+1][col]==1){
              List<Integer> loc = List.of(row+1,col);
              if (!added.contains(loc)){
                stack.add(loc);
                added.add(loc);
              }
            } 
            if (col>0 && grid[row][col-1]==1){
              List<Integer> loc = List.of(row,col-1);
              if (!added.contains(loc)){
                stack.add(loc);
                added.add(loc);
              }
            } 
            if (col<clen-1 && grid[row][col+1]==1) {
              List<Integer> loc = List.of(row,col+1);
              if (!added.contains(loc)){
                stack.add(loc);
                added.add(loc);
              }
            }
          }
          maxArea = maxArea>=area?maxArea:area;
        }
      }
    }
    return maxArea;
  }
